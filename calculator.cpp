#include <iostream>

using namespace std;

int main() {
	int first_num;
	int second_num;
	
	first_num = 29;
	second_num = 30;
	
	cout << "Number first_num: " << first_num << endl;
	cout << "Number second_num: " << second_num << endl;
	
	int sum_of_both;
	sum_of_both = first_num + second_num;
	
	cout << "The sum of " << first_num << " and " << second_num << " is : " << sum_of_both << endl;
	
	return 0;
}